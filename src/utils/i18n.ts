export type MessageKey = keyof typeof messages.en;

export function setLanguage(language: unknown): void {
  if (language === "en" || language === "zh-CN") currentLanguage = language;
}

export function languageFromState(state: State | undefined): Language {
  return state?.language === "en" ? "en" : "zh-CN";
}

export function getLanguage(): Language {
  return currentLanguage;
}

export function t(key: MessageKey, params: Params = {}): string {
  const template = messages[currentLanguage][key] ?? messages.en[key] ?? key;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_match, name: string) => String(params[name] ?? `{${name}}`));
}

export function formatAgent(agent: string): string {
  if (agent === "claude") return "Claude";
  if (agent === "codex") return "Codex";
  return agent;
}
